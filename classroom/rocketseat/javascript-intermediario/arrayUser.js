let users = [];

// push adiciona um indice/item ao final da array
users.push("Iago");
users.push("Fernanda");
users.push("Marcos");

// adicona um item/indice ao inicio da array
users.unshift("Diego");

// como remover um item/indice do inicio da array
users.shift()

// remover do final da array
users.pop()

console.log(users)

